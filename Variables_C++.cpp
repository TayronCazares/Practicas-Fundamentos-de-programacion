#include <iostream>
#include <string>

int main() {
    // Entero
    int entero = 5;
    std::cout << "Entero: " << entero << std::endl;
    
    // Flotante
    float flotante = 3.14;
    std::cout << "Flotante: " << flotante << std::endl;
    
    // Caracter
    char caracter = 'A';
    std::cout << "Caracter: " << caracter << std::endl;
    
    // Cadena de caracteres (string)
    std::string cadena = "Hola, mundo!";
    std::cout << "Cadena: " << cadena << std::endl;
    
    return 0;
}
